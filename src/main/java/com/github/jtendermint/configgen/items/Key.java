package com.github.jtendermint.configgen.items;

public class Key {
	private String type;
	private String data;

	public Key() {

	}

	public void setType(String type) {
		this.type = type;
	}

	public String getType() {
		return this.type;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getData() {
		return this.data;
	}
}
